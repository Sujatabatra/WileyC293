package com.sujata.demo;

public class MyDemoClass {

	private int integerInstanceVariable;
	private double doubleInstanceVariable;
	private char charInstanceVariable;
	private boolean booleanInstanceVariable;
	private String objectInstanceVariable;
	public int getIntegerInstanceVariable() {
		return integerInstanceVariable;
	}
	public void setIntegerInstanceVariable(int integerInstanceVariable) {
		this.integerInstanceVariable = integerInstanceVariable;
	}
	public double getDoubleInstanceVariable() {
		return doubleInstanceVariable;
	}
	public void setDoubleInstanceVariable(double doubleInstanceVariable) {
		this.doubleInstanceVariable = doubleInstanceVariable;
	}
	public char getCharInstanceVariable() {
		return charInstanceVariable;
	}
	public void setCharInstanceVariable(char charInstanceVariable) {
		this.charInstanceVariable = charInstanceVariable;
	}
	public boolean isBooleanInstanceVariable() {
		return booleanInstanceVariable;
	}
	public void setBooleanInstanceVariable(boolean booleanInstanceVariable) {
		this.booleanInstanceVariable = booleanInstanceVariable;
	}
	public String getObjectInstanceVariable() {
		return objectInstanceVariable;
	}
	public void setObjectInstanceVariable(String objectInstanceVariable) {
		this.objectInstanceVariable = objectInstanceVariable;
	}
	
	
	
}
